from __future__ import annotations

import csv
import subprocess
import sys
from pathlib import Path


ROOT = Path(__file__).resolve().parents[1]
OUT = ROOT / "experiments" / "output"
PAPER = ROOT / "paper"
OUT.mkdir(parents=True, exist_ok=True)
PAPER.mkdir(parents=True, exist_ok=True)


def _run_script(script_name: str) -> None:
    subprocess.run(
        [sys.executable, str(ROOT / "experiments" / script_name)],
        check=True,
        cwd=ROOT,
    )


def _read_csv(path: Path) -> list[dict[str, str]]:
    with path.open(newline="", encoding="utf-8") as f:
        return list(csv.DictReader(f))


def _latex_escape(value: object) -> str:
    text = str(value)
    return (
        text.replace("\\", r"\textbackslash{}")
        .replace("_", r"\_")
        .replace("%", r"\%")
        .replace("&", r"\&")
        .replace("#", r"\#")
        .replace("$", r"\$")
    )


def _latex_table(
    *,
    label: str,
    caption: str,
    headers: list[str],
    rows: list[dict[str, str]],
    columns: list[str],
) -> str:
    colspec = "l" * len(columns)
    lines = [
        r"\begin{table}[t]",
        r"\centering",
        r"\small",
        r"\begin{tabular}{" + colspec + r"}",
        r"\hline",
        " & ".join(_latex_escape(h) for h in headers) + r" \\",
        r"\hline",
    ]

    for row in rows:
        lines.append(" & ".join(_latex_escape(row[c]) for c in columns) + r" \\")

    lines.extend([
        r"\hline",
        r"\end{tabular}",
        r"\caption{" + caption + r"}",
        r"\label{" + label + r"}",
        r"\end{table}",
    ])
    return "\n".join(lines) + "\n"


def _markdown_table(headers: list[str], rows: list[dict[str, str]], columns: list[str]) -> str:
    lines = []
    lines.append("| " + " | ".join(headers) + " |")
    lines.append("| " + " | ".join(["---"] * len(headers)) + " |")
    for row in rows:
        lines.append("| " + " | ".join(str(row[c]) for c in columns) + " |")
    return "\n".join(lines) + "\n"


def _select_decomposition_rows(rows: list[dict[str, str]]) -> list[dict[str, str]]:
    keep = {"0", "10", "50", "100", "500"}
    selected = [row for row in rows if row["noise_blocks"] in keep]
    return selected


def _select_solver_rows(rows: list[dict[str, str]]) -> list[dict[str, str]]:
    keep_cases = {
        "pm_plus_0_disconnected",
        "pm_plus_100_disconnected",
        "pm_plus_10_connected_ladder",
    }
    return [row for row in rows if row["case"] in keep_cases]


def generate_tables(*, run_benchmarks: bool = True) -> tuple[Path, Path]:
    """Generate paper benchmark tables.

    This script intentionally writes generated files rather than editing the
    paper body by hand. The paper can include the generated LaTeX table file.
    """
    if run_benchmarks:
        _run_script("benchmark_decomposition.py")
        _run_script("compare_solvers.py")

    decomp_csv = OUT / "benchmark_decomposition.csv"
    solvers_csv = OUT / "compare_solvers.csv"

    if not decomp_csv.exists() or not solvers_csv.exists():
        raise FileNotFoundError("benchmark CSV files missing; run benchmark scripts first.")

    decomp_rows = _select_decomposition_rows(_read_csv(decomp_csv))
    solver_rows = _select_solver_rows(_read_csv(solvers_csv))

    decomp_columns = [
        "noise_blocks",
        "contexts",
        "observables",
        "components",
        "kernel_contexts",
        "t_compress_decomp_ms",
        "t_compress_nodecomp_ms",
        "same_kernel",
    ]
    decomp_headers = [
        "noise",
        "ctx",
        "obs",
        "comp",
        "kernel ctx",
        "decomp ms",
        "global ms",
        "same",
    ]

    solver_columns = [
        "case",
        "solver",
        "contexts",
        "observables",
        "contextual",
        "kernel_contexts",
        "verified",
        "elapsed_ms",
    ]
    solver_headers = [
        "case",
        "solver",
        "ctx",
        "obs",
        "contextual",
        "kernel ctx",
        "verified",
        "ms",
    ]

    latex_parts = [
        "% Auto-generated by experiments/render_paper_tables.py.",
        "% Do not edit manually.",
        "",
        _latex_table(
            label="tab:decomposition-benchmark",
            caption=(
                "Synthetic decomposition benchmark. A Peres--Mermin core is augmented "
                "with disconnected noncontextual checks. Times are local median wall "
                "times in milliseconds and are intended as regression diagnostics, not "
                "hardware performance claims."
            ),
            headers=decomp_headers,
            rows=decomp_rows,
            columns=decomp_columns,
        ),
        "",
        _latex_table(
            label="tab:solver-comparison",
            caption=(
                "Synthetic solver comparison on selected instances. The purpose is to "
                "compare internal exact solver behavior and catch regressions; the table "
                "does not imply quantum resource advantage."
            ),
            headers=solver_headers,
            rows=solver_rows,
            columns=solver_columns,
        ),
    ]

    markdown_parts = [
        "# Generated paper benchmark tables",
        "",
        "Generated from `experiments/output/*.csv`.",
        "",
        "## Decomposition benchmark",
        "",
        _markdown_table(decomp_headers, decomp_rows, decomp_columns),
        "",
        "## Solver comparison",
        "",
        _markdown_table(solver_headers, solver_rows, solver_columns),
    ]

    tex_path = PAPER / "generated_benchmark_tables.tex"
    md_path = PAPER / "generated_benchmark_tables.md"

    tex_path.write_text("\n".join(latex_parts) + "\n", encoding="utf-8")
    md_path.write_text("\n".join(markdown_parts) + "\n", encoding="utf-8")

    return tex_path, md_path


def main() -> None:
    tex, md = generate_tables(run_benchmarks=True)
    print(f"Wrote: {tex}")
    print(f"Wrote: {md}")


if __name__ == "__main__":
    main()
